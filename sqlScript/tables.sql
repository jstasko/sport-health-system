DECLARE
    cursor cur_state is (select code, sys.json_web_service_geo(code) as json from states_json) for update;
begin
    for riadok in cur_state
        loop
            update states_json set doc=riadok.json where current of cur_state;
        end loop;
    commit;
end;
/

